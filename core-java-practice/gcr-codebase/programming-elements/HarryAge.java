


//Creates a class of HarryAge to calculate Harrys's age
public class HarryAge{
    public static void main(String[] args) {
        int harryBirthYear = 2000; //Declare a variable to store Harrys's birth year
        int currentYear = 2024; //Declare a variable to store the current year 


        // Declare a variable harryAge to calculate Harrys's age by subtracting birth year from current year 
        int harryAge = currentYear - harryBirthYear; 

        //Display Harrys's age
        System.out.println("Harry's age in 2024 is ___ " + harryAge);
    }
}